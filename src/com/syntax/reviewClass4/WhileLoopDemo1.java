public class WhileLoopDemo1 {

	public static void main(String[] args) {
		
		//Loops
		//Print the 1st 10 numbers 
		
		int x = 1;   // starting point
		while (x <= 10 ){	
			System.out.println(x);
			 x ++;     // or we can type x = x + 1; to print even numbers do x = x +2;
			 
	// to print numbers from 10 to 1 change line 10 to x = 10, then changes line 11 to x >= 1
    // and then change line 13 to x --;
			 
	// To print odd numbers from 10 to 1, change line 10 is x = 9 and line 11 is x >= 1 but 
	// change line 13 to y = y-2
		}
	}
}